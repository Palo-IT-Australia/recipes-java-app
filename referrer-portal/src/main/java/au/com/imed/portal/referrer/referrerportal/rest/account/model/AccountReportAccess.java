package au.com.imed.portal.referrer.referrerportal.rest.account.model;

public class AccountReportAccess {
	private String reportUri, orderUri, patientUri;

	public String getReportUri() {
		return reportUri;
	}

	public void setReportUri(String reportUri) {
		this.reportUri = reportUri;
	}

	public String getOrderUri() {
		return orderUri;
	}

	public void setOrderUri(String orderUri) {
		this.orderUri = orderUri;
	}

	public String getPatientUri() {
		return patientUri;
	}

	public void setPatientUri(String patientUri) {
		this.patientUri = patientUri;
	}
	
	
}
